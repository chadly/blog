// Generates content/generated/{posts,projects}.ts with static imports so the
// bundler picks up each MDX file and its colocated assets/components.
import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";

const root = path.join(process.cwd(), "content");
const outDir = path.join(root, "generated");
fs.mkdirSync(outDir, { recursive: true });

const esc = s => JSON.stringify(s ?? null);

const excerpt = (content, len = 140) => {
	const text = content
		.replace(/^import .*$/gm, "")
		.replace(/```[\s\S]*?```/g, "")
		.replace(/<[^>]+>/g, "")
		.replace(/!\[[^\]]*\]\([^)]*\)/g, "")
		.replace(/\[([^\]]*)\]\([^)]*\)/g, "$1")
		.replace(/[#*_`>]/g, "")
		.replace(/\s+/g, " ")
		.trim();
	return text.length > len ? `${text.slice(0, len).replace(/\s+\S*$/, "")}…` : text;
};

const readingTime = content =>
	Math.max(1, Math.round(content.trim().split(/\s+/).length / 200));

// ---- posts ----
const postsDir = path.join(root, "posts");
const posts = [];

for (const entry of fs.readdirSync(postsDir, { withFileTypes: true }).sort((a, b) => a.name.localeCompare(b.name))) {
	const isDir = entry.isDirectory();
	const file = isDir
		? path.join(postsDir, entry.name, "index.mdx")
		: path.join(postsDir, entry.name);
	if (!file.endsWith(".mdx") || !fs.existsSync(file)) continue;

	const slug = isDir ? entry.name : entry.name.replace(/\.mdx$/, "");
	const { data, content } = matter(fs.readFileSync(file, "utf8"));
	posts.push({
		slug,
		importPath: isDir ? `../posts/${slug}/index.mdx` : `../posts/${slug}.mdx`,
		cover: data.cover
			? `../posts/${slug}/${data.cover.replace(/^\.\//, "")}`
			: null,
		data,
		description: data.description || excerpt(content),
		readingTime: readingTime(content)
	});
}

let out = `// GENERATED by scripts/generate-content-map.mjs — do not edit\nimport type { StaticImageData } from "next/image";\nimport type { MDXProps } from "mdx/types";\nimport type { JSX } from "react";\n\nexport interface Post {\n\tslug: string;\n\tid: string;\n\ttitle: string;\n\tdescription: string;\n\tdate: string;\n\ttwitterId: string | null;\n\tredirectFrom: string[];\n\treadingTime: number;\n\tcover: StaticImageData | null;\n\tload: () => Promise<{ default: (props: MDXProps) => JSX.Element }>;\n}\n\n`;

posts.forEach((p, i) => {
	if (p.cover) out += `import cover${i} from ${esc(p.cover)};\n`;
});

out += `\nexport const posts: Post[] = [\n`;
out += posts
	.map(
		(p, i) => `\t{
\t\tslug: ${esc(p.slug)},
\t\tid: ${esc(p.data.id)},
\t\ttitle: ${esc(p.data.title)},
\t\tdescription: ${esc(p.description)},
\t\tdate: ${esc(String(p.data.date))},
\t\ttwitterId: ${esc(p.data.twitterId ? String(p.data.twitterId) : null)},
\t\tredirectFrom: ${JSON.stringify(p.data.redirect_from ?? [])},
\t\treadingTime: ${p.readingTime},
\t\tcover: ${p.cover ? `cover${i}` : "null"},
\t\tload: () => import(${esc(p.importPath)})
\t}`
	)
	.join(",\n");
out += "\n];\n";

fs.writeFileSync(path.join(outDir, "posts.ts"), out);

// ---- projects ----
const projectsDir = path.join(root, "projects");
const projects = [];

for (const name of fs.readdirSync(projectsDir).sort()) {
	if (!name.endsWith(".mdx")) continue;
	const { data } = matter(fs.readFileSync(path.join(projectsDir, name), "utf8"));
	projects.push({
		slug: name.replace(/\.mdx$/, ""),
		importPath: `../projects/${name}`,
		logo: data.logo ? `../projects/${data.logo}` : null,
		data
	});
}

projects.sort((a, b) => (a.data.sorting ?? 999) - (b.data.sorting ?? 999));

let pout = `// GENERATED by scripts/generate-content-map.mjs — do not edit\nimport type { StaticImageData } from "next/image";\nimport type { MDXProps } from "mdx/types";\nimport type { JSX } from "react";\n\nexport interface Project {\n\tslug: string;\n\theadline: string;\n\thref: string;\n\tlogo: StaticImageData | null;\n\tload: () => Promise<{ default: (props: MDXProps) => JSX.Element }>;\n}\n\n`;

projects.forEach((p, i) => {
	if (p.logo) pout += `import logo${i} from ${esc(p.logo)};\n`;
});

pout += `\nexport const projects: Project[] = [\n`;
pout += projects
	.map(
		(p, i) => `\t{
\t\tslug: ${esc(p.slug)},
\t\theadline: ${esc(p.data.headline)},
\t\thref: ${esc(p.data.href)},
\t\tlogo: ${p.logo ? `logo${i}` : "null"},
\t\tload: () => import(${esc(p.importPath)})
\t}`
	)
	.join(",\n");
pout += "\n];\n";

fs.writeFileSync(path.join(outDir, "projects.ts"), pout);

console.log(`generated ${posts.length} posts, ${projects.length} projects`);
